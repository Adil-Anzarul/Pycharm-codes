
# it will print from 0 to 19 (20 is excluded)
for i in range(20):
    print(i,end="  ")


print("\n")

#it will print from 5 to 19
for j in range(5,20):
    print(j,end="  ")

print("\n")
#it will print in reverse
# here 0 will be excluded

for k in range(20,0,-1):
    print(k,end="  ")

print("\n")

#it will print 2,4,.....,16,18
for l in range(2,20,2):
    print(l,end="  ")

print("\n")


 #it will print 20,18,16,.....,4,2,0
for m in range(20,-1,-2):
    print(m,end="  ")

print("\n")


#for printing alphabet
for n in range(65,91):
    print(chr(n),end=" ")
